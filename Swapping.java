
public class Swapping {
	public static void main(String args[])
	{
		String s1="Gamya";
		String s2="Keerthana";
		String temp="";
		System.out.println("****Before Swap****");
		System.out.println(s1);
		System.out.println(s2);
		System.out.println("****After swap****");
		temp=s1;
		s1=s2;
		s2=temp;
		System.out.println(s1);
		System.out.println(s2);
	}
}
